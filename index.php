<script>
const drPhrases = [
    "Excellence. Elegance. Empire.",
    "Where Golf Meets Royalty.",
    "India's Premier Golf Franchise.",
    "Luxury. Leadership. Legacy.",
    "A Movement Beyond Sport.",
    "Redefining Indian Golf Culture."
];
let pi = 0, ci = 0, del = false;
const el = document.getElementById('drTw');
function drType() {
    const cur = drPhrases[pi];
    if (!del) {
        el.textContent = cur.slice(0, ci + 1);
        ci++;
        if (ci === cur.length) { del = true; setTimeout(drType, 2400); return; }
    } else {
        el.textContent = cur.slice(0, ci - 1);
        ci--;
        if (ci === 0) { del = false; pi = (pi + 1) % drPhrases.length; }
    }
    setTimeout(drType, del ? 38 : 85);
}
drType();
</script>
